<script>
	export default {
		data() {
			return {
				// 基础表单数据
				baseFormData: {
					Account: '',
					Password: '',
					introduction: '景德镇市第三人民医生',

				},
			}
		},
		onLoad() {
			
		},
		methods: {
			userLogin() {
				this.$U.gotoPageAndClosePage('/pages/login/index');
			},
			login() {
				const { Account, Password} = this.baseFormData
				if(this.$U.dateUtils.isEmpty(Account)) {
					this.$U.checkTip('账户不能为空！')
					return;
				}
				if(this.$U.dateUtils.isEmpty(Password)) {
					this.$U.checkTip('密码不能为空！')
					return;
				}
				this.$H.post('/api/APP/WXUser/DoctorLogin', this.baseFormData, {}, {show: true, text: '登录中'})
					.then(res => {
						if(res.Code === 200) {
							this.$store.dispatch('app/setUserType', 'doctor')
							this.$store.dispatch('app/setToken', res.Data)
							// 获取用户信息
							this.getUserInfo();
							uni.setStorage({
								key: 'token',
								data: { Token: res.Data },
								success: () => {
									this.$U.gotoPageTab('/pages/index/index');
									// 显示tabbar
									uni.showTabBar({ animation: true });
								}
							})
						}
					})
			},
			// 获取用户信息
			getUserInfo() {
				this.$H.get('/api/APP/WXUser/GetDoctorInfo')
					.then(res => {
						if(res.Data) {
							this.$store.dispatch('userDoctor/setUserInfo', res.Data);
							uni.setStorage({
								key: 'doctor-userinfo',
								data: res.Data
							})
						}
					}).catch(err => {
						console.log('err:', err)
					})
			},
		},
	}
</script>

